// Math Object

console.log(Math.PI);
console.log(Math.E);
console.log(Math.abs(7));
console.log(Math.abs(-7));
console.log(Math.ceil(7.2));
console.log(Math.floor(7.2));
console.log(Math.round(7.2));
console.log(Math.sqrt(49));
console.log(Math.random());
console.log(Math.pow(3,2));

// This will show the use of the Math Object
// Math.PI is a constant that returns the value of pi
// Math.E is a constant that returns the value of Euler's number
// Math.abs() returns the absolute value of a number
// Math.ceil() returns the smallest integer greater than or equal to a number
// Math.floor() returns the largest integer less than or equal to a number
// Math.round() returns the value of a number rounded to the nearest integer
// Math.sqrt() returns the square root of a number
// Math.random() returns a random number between 0 and 1
// Math.pow() returns the value of a number raised to a power